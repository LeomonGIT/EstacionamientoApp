package edu.ulima.estacionapp.Bean;

public class Usuario {

    String nombre;
    String correo;
    int type;
    String userId;
    String id;
    public Usuario(){}
    public Usuario(String nombre, String correo, int type) {
        this.nombre = nombre;
        this.correo = correo;
        this.type = type;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
